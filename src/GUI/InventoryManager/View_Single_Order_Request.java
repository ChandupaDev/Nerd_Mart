    private void loadOrderRequests() {
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `order_request` "
                    + "INNER JOIN `order_request_item` ON `order_request`.`id`=`order_request_item`.`order_request_id` "
                    + "INNER JOIN `supplier` ON `order_request`.`supplier_id`=`supplier`.`id` "
                    + "INNER JOIN `user` ON `order_request`.`user_id`=`user`.`id` "
                    + "INNER JOIN `product` ON `order_request_item`.`product_barcode`=`product`.`barcode` "
                    + "WHERE `order_request`.`id`='" + orderId + "' ORDER BY `order_request_item`.`id` ASC ");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                jLabel2.setText(resultSet.getString("date"));
                jLabel5.setText(orderId);

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("order_request_item.id"));
                vector.add(resultSet.getString("supplier.name"));
                vector.add(resultSet.getString("user.username"));
                vector.add(resultSet.getString("unique_id"));
                vector.add(resultSet.getString("product_barcode"));
                vector.add(resultSet.getString("product.name"));
                vector.add(resultSet.getString("qty"));

                model.addRow(vector);
            }
            jTable1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

