    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int selected_row = jTable1.getSelectedRow();
        if (selected_row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            company_name = String.valueOf(jTable1.getValueAt(selected_row, 0));
            String mobile = String.valueOf(jTable1.getValueAt(selected_row, 1));
            String province = String.valueOf(jTable1.getValueAt(selected_row, 2));
            String city = String.valueOf(jTable1.getValueAt(selected_row, 3));
            String line1 = String.valueOf(jTable1.getValueAt(selected_row, 4));
            String line2 = String.valueOf(jTable1.getValueAt(selected_row, 5));
            String company_id = companyIdMap.get(company_name);

            Update_Com_view cv = new Update_Com_view(this);
            cv.get(company_id, company_name, mobile, province, city, line1, line2);
            cv.setVisible(true);
        }

    }//GEN-LAST:event_jButton4ActionPerformed

