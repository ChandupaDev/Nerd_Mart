    public void loadCompanies() {
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `company` INNER JOIN `city` ON `company`.`city_id` = `city`.`id` \n"
                    + "INNER JOIN `province` ON `city`.`province_id`=`province`.`id`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            while (resultSet.next()) {

                String name = resultSet.getString("name");
                String mobile = resultSet.getString("mobile");
                String province = resultSet.getString("province.name");
                String city = resultSet.getString("city.name");
                String line1 = resultSet.getString("line1");
                String line2 = resultSet.getString("line2");
                companyIdMap.put(name, resultSet.getString("company.id"));

                Vector v = new Vector();

                v.add(name);
                v.add(mobile);
                v.add(province);
                v.add(city);
                v.add(line1);
                v.add(line2);

                model.addRow(v);

            }
        } catch (Exception e) {
            logs.logger.log(Level.SEVERE, String.valueOf(e));
        }

    }

