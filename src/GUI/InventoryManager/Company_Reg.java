    public void loadCity() {
        try {
            java.sql.ResultSet resultSet = MySQL.execute("SELECT * FROM `city`");
            DefaultComboBoxModel model = (DefaultComboBoxModel) jComboBox2.getModel();
            model.removeAllElements();

            Vector v = new Vector();
            v.add("Select");

            while (resultSet.next()) {
                v.add(resultSet.getString("name"));
                cityMap.put(resultSet.getString("name"), resultSet.getString("id"));
            }

            model.addAll(v);
            jComboBox2.setSelectedIndex(0);

        } catch (Exception e) {
            logs.logger.log(Level.SEVERE, String.valueOf(e));
        }
    }

    public void loadProvince() {
        try {
            java.sql.ResultSet resultSet = MySQL.execute("SELECT * FROM `province`");
            DefaultComboBoxModel model = (DefaultComboBoxModel) jComboBox1.getModel();
            model.removeAllElements();

            Vector v = new Vector();
            v.add("Select");
            while (resultSet.next()) {
                v.add(resultSet.getString("name"));
                provinceMap.put(resultSet.getString("name"), resultSet.getString("id"));
            }
            model.addAll(v);
            jComboBox1.setSelectedIndex(0);

        } catch (Exception e) {
            logs.logger.log(Level.SEVERE, String.valueOf(e));
        }

    }

