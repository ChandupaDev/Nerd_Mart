    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String companyName = jTextField1.getText();
        String contactNumber = jTextField2.getText();
        String city = String.valueOf(jComboBox2.getSelectedItem());
        String province = String.valueOf(jComboBox1.getSelectedItem());
        String line1 = jTextField4.getText();
        String line2 = jTextField5.getText();

        if (companyName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Company Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (contactNumber.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Company Contct Number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!contactNumber.matches("^07[01245678]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "Invalide Contact Number Contct Number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (province.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Company Province", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Company City", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (line1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Company Address Line 1", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (line2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Company Address Line 2", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet resultSet = MySQL.execute("SELECT * FROM company WHERE `name`='" + companyName + "' AND `mobile`='" + contactNumber + "'");
                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Company already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.execute("INSERT INTO `company` (`name`,`mobile`,`line1`,`line2`,`city_id`) \n"
                            + " VALUES ('" + companyName + "','" + contactNumber + "','" + line1 + "','" + line2 + "','" + cityMap.get(city) + "')");
                }

            } catch (Exception e) {
                logs.logger.log(Level.SEVERE, String.valueOf(e));
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

