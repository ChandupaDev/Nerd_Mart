    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String nic = jTextField1.getText();
        Date date = jDateChooser1.getDate();

        if (nic.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter employee nic", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (date == null) {
            JOptionPane.showMessageDialog(this, "Please select the date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                logs.startLogger();

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);

                SimpleDateFormat sdf = new SimpleDateFormat("yyy-MM-dd");
                ResultSet resultSet = MySQL.execute("SELECT `employee`.`nic`, `employee`.`fname`, `employee`.`lname`, `attendance`.`arrived_at`, `attendance`.`left_at`, `attendance`.`date`, attendance_status.status "
                        + " FROM `attendance` "
                        + "INNER JOIN `employee` ON `attendance`.`employee_nic` = `employee`.`nic` INNER JOIN attendance_status ON attendance.attendance_status_id = attendance_status.id"
                        + "WHERE `attendance`.`employee_nic` = '" + nic + "' AND `attendance`.`date` = '" + sdf.format(date) + "'");

                while (resultSet.next()) {
                    Vector v = new Vector();
                    v.add(resultSet.getString("employee.nic"));
                    String emp_name = resultSet.getString("fname")+" "+resultSet.getString("lname");
                    v.add(emp_name);
                    v.add(resultSet.getTime("arrived_at"));
                    v.add(resultSet.getTime("left_at"));
                    v.add(resultSet.getDate("date"));
                    v.add(resultSet.getString("attendance_status.status"));
                    model.addRow(v);

                }

                reset();

            } catch (Exception e) {
                logs.logger.log(Level.SEVERE, String.valueOf(e));
            }

        }


    }//GEN-LAST:event_jButton2ActionPerformed


