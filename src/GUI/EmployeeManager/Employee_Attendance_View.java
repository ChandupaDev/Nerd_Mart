    public void loadAttendance(){
        try {
             DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);

                SimpleDateFormat sdf = new SimpleDateFormat("yyy-MM-dd");
                ResultSet resultSet = MySQL.execute("SELECT `employee`.`nic`, `employee`.`fname`, `employee`.`lname`,`attendance`.`arrived_at`, `attendance`.`left_at`, `attendance`.`date`, attendance_status.status "
                        + " FROM `attendance` "
                        + "INNER JOIN `employee` ON `attendance`.`employee_nic` = `employee`.`nic` INNER JOIN attendance_status ON attendance.attendance_status_id = attendance_status.id ");

                while (resultSet.next()) {
                    Vector v = new Vector();
                    v.add(resultSet.getString("employee.nic"));
                    String emp_name = resultSet.getString("fname")+" "+resultSet.getString("lname");
                    v.add(emp_name);
                    v.add(resultSet.getTime("arrived_at"));
                    v.add(resultSet.getTime("left_at"));
                    v.add(resultSet.getDate("date"));
                    v.add(resultSet.getString("attendance_status.status"));
                    model.addRow(v);

                }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

